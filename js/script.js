// 🎲 Генерация характеристик 
document.getElementById("stats-btn").addEventListener("click", () => {
    const rollStat = () => {
        // классический метод D&D: 4d6, убираем наименьший куб
        let rolls = [1, 2, 3, 4].map(() => Math.floor(Math.random() * 6) + 1);
        rolls.sort((a, b) => a - b);
        rolls.shift(); // убираем минимальный куб
        return rolls.reduce((a, b) => a + b, 0);
    };

    document.getElementById("stat-str").textContent = rollStat();
    document.getElementById("stat-dex").textContent = rollStat();
    document.getElementById("stat-con").textContent = rollStat();
    document.getElementById("stat-int").textContent = rollStat();
    document.getElementById("stat-wis").textContent = rollStat();
    document.getElementById("stat-cha").textContent = rollStat();

});
